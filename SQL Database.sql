SET FOREIGN_KEY_CHECKS = 0;

CREATE TABLE IF NOT EXISTS `dim_produk` (
  `id_produk` INT NOT NULL AUTO_INCREMENT,
  `kode_produk` VARCHAR(20),
  `nama_produk` VARCHAR(100),
  `kategori` VARCHAR(50),
  `harga` DECIMAL(10,2),
  PRIMARY KEY (`id_produk`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `dim_pelanggan` (
  `id_pelanggan` INT NOT NULL AUTO_INCREMENT,
  `kode_pelanggan` VARCHAR(20),
  `nama_pelanggan` VARCHAR(100),
  `jenis_kelamin` CHAR(1),
  `kota` VARCHAR(50),
  PRIMARY KEY (`id_pelanggan`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `dim_waktu` (
  `id_waktu` INT NOT NULL AUTO_INCREMENT,
  `tanggal` DATE,
  `tahun` INT,
  `bulan` INT,
  `bulan_nama` VARCHAR(20),
  `kuartal` INT,
  PRIMARY KEY (`id_waktu`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `fact_penjualan` (
  `id_penjualan` INT NOT NULL AUTO_INCREMENT,
  `id_produk` INT,
  `id_pelanggan` INT,
  `id_waktu` INT,
  `jumlah` INT,
  `harga_satuan` DECIMAL(10,2),
  `total_harga` DECIMAL(10,2),
  PRIMARY KEY (`id_penjualan`),
  INDEX `idx_fact_produk` (`id_produk`),
  INDEX `idx_fact_pelanggan` (`id_pelanggan`),
  INDEX `idx_fact_waktu` (`id_waktu`),
  CONSTRAINT `fact_penjualan_id_produk_fkey` FOREIGN KEY (`id_produk`) REFERENCES `dim_produk`(`id_produk`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fact_penjualan_id_pelanggan_fkey` FOREIGN KEY (`id_pelanggan`) REFERENCES `dim_pelanggan`(`id_pelanggan`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `fact_penjualan_id_waktu_fkey` FOREIGN KEY (`id_waktu`) REFERENCES `dim_waktu`(`id_waktu`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

